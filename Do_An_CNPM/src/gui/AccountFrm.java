    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (new AccountBUS().checkAccount(txtUser.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Mã nhân viên đã tồn tại");
        }
        else if (txtUser.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập mã nhân viên");
        } 
        else if (txtMk.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập mật khẩu");
        } 
        else if (txtReMk.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập lại mật khẩu");
        } 
        else if (txtName.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập họ tên nhân viên");
        } 
        else if (txtBirthday.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập ngày sinh");
        } 
        else if (txtAddress.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập địa chỉ");
        }
        else if (txtPhone.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa số điện thoại");
        } 
        else {
            Account a = new Account();
            try {
                a.setUserName(txtUser.getText());
                a.setPassWord(txtMk.getText());
                a.setFullName(txtName.getText());
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                a.setBirthDay(sdf.parse(txtBirthday.getText()));
                a.setAddress(txtAddress.getText());
                a.setPhone(txtPhone.getText());
                list.add(a);
                showResult();
                if (new AccountBUS().addAccount(a)) {
                    JOptionPane.showMessageDialog(rootPane, "Thành công");
                    new AccountFrm().setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Failed");
                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

