    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (txtUser.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa chọn nhân viên");
        } else if (txtMk.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa chọn nhân viên");
        } else if (txtReMk.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa chọn nhân viên");
        } else if (txtName.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa chọn nhân viên");
        } else if (txtBirthday.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa chọn nhân viên");
        } else if (txtAddress.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa chọn nhân viên");
        } else if (txtPhone.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa chọn nhân viên");
        } else {
            int input = JOptionPane.showConfirmDialog(rootPane, "Bạn có chăc muốn xóa ?", "Exit", JOptionPane.YES_NO_OPTION);
            if (input == 0) {
                Account a = new Account();
                a.setUserName(txtUser.getText());
                if (new AccountBUS().deleteAccount(a)) {
                    JOptionPane.showMessageDialog(rootPane, "Thành công");
                    new AccountFrm().setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Lỗi");
                }
            } else {
                return;
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

