    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String a = (String) jComboBox1.getSelectedItem();
        list=TableBUS.getTable();
        selectIndex = tblTable.getSelectedRow();
        if (a.equals(" ")){
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa chọn loại bàn cần thêm");
        }
        else {
            if (TableBUS.addTable(a)){
                JOptionPane.showMessageDialog(rootPane, "Thành công");
                new TableAdminFrm().setVisible(true);
                this.dispose();
            }
            else {
                JOptionPane.showMessageDialog(rootPane, "Lỗi");
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        selectIndex = tblTable.getSelectedRow();
        if (selectIndex == -1){
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa chọn bàn muốn xóa");
        }
        else {
            int input = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc muốn xóa ?", "Exit", JOptionPane.YES_NO_OPTION);
            if (input == 0) {
                if (TableBUS.deleteTable(list.get(selectIndex).getSoban())) {
                    JOptionPane.showMessageDialog(rootPane, "Thành công");
                    new TableAdminFrm().setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Lỗi");
                }
            } else {
                return;
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

