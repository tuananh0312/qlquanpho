    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String a = (String) jComboBox1.getSelectedItem();
        list=TableBUS.getTable();
        selectIndex = tblTable.getSelectedRow();
        if (a.equals(" ")){
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa chọn bàn cần chỉnh sửa");
        }
        else {
            if (TableBUS.updateTable(a, list.get(selectIndex).getSoban())){
                JOptionPane.showMessageDialog(rootPane, "Thành công");
                new TableAdminFrm().setVisible(true);
                this.dispose();
            }
            else {
                JOptionPane.showMessageDialog(rootPane, "Lỗi");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

